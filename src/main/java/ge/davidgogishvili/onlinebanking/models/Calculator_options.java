package ge.davidgogishvili.onlinebanking.models;

public class Calculator_options {
    public static Integer jami (Integer a, Integer b) {
        return a+b;
    }

    public static Integer minusi (Integer a, Integer b) {
        return a-b;
    }

    public static Integer namravli (Integer a, Integer b) {
        return a*b;
    }

    public static Integer gakofili (Integer a, Integer b) {
        return b/a;
    }
}
